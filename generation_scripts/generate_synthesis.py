def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--mode", choices=["trace", "synthesis", "all"], default="all")
    parser.add_argument("--limit", type=int, default=30,
                        help="Max synthesis tasks to generate (each costs ~1 API call)")
    args = parser.parse_args()

    BASE.mkdir(parents=True, exist_ok=True)

    if args.mode in ("trace", "all"):
        print("\n── Trace-derived tasks ──")
        td_tasks = build_trace_derived_tasks()
        out = BASE / "trace_derived_tasks.jsonl"
        with open(out, "w") as f:
            for t in td_tasks:
                f.write(json.dumps(t) + "\n")
        print(f"  {len(td_tasks)} trace-derived tasks → {out}")

    if args.mode in ("synthesis", "all"):
        if not OPENROUTER_API_KEY:
            print("\nSkipping synthesis: OPENROUTER_API_KEY not set")
            return
        print(f"\n── Multi-LLM synthesis ({args.limit} seeds) ──")
        raw_tasks = build_synthesis_tasks(limit=args.limit)
        raw_out = BASE / "synthesis_tasks_raw.jsonl"
        with open(raw_out, "w") as f:
            for t in raw_tasks:
                f.write(json.dumps(t) + "\n")
        print(f"  {len(raw_tasks)} raw synthesis tasks → {raw_out}")

        print("\n── Judge filtering synthesis tasks ──")
        from judge_filter import filter_tasks
        kept, rejected = filter_tasks(raw_tasks, generator_model=GEN_MODEL)
        filtered_out = BASE / "synthesis_tasks_filtered.jsonl"
        with open(filtered_out, "w") as f:
            for t in kept:
                f.write(json.dumps(t) + "\n")
        rej_out = BASE / "synthesis_tasks_rejected.jsonl"
        with open(rej_out, "w") as f:
            for t in rejected:
                f.write(json.dumps(t) + "\n")
        print(f"  {len(kept)} kept → {filtered_out}")
        print(f"  {len(rejected)} rejected → {rej_out}")


if __name__ == "__main__":
    main()
