def build_trace_derived_tasks() -> list[dict]:
    """Convert probe seeds into structured eval tasks without LLM calls."""
    tasks = []
    for seed in TRACE_SEEDS:
        brief = seed["brief"].copy()
        hard_constraints = seed["hard"]
        soft_dims = [{
            "id": "SD-A",
            "dimension": "policy_adherence",
            "description": "Agent follows the relevant policy constraint",
            "score_range": "1-5",
            "threshold": 4,
            "judge_prompt": f"Agent output:\n{{output}}\n\n{seed['soft_prompt']}",
        }]

        if "bench" in seed:
            bench_context = seed["bench"]
        else:
            bench_context = None

        if "competitor_gap" in seed:
            gap_brief = seed["competitor_gap"]
        else:
            gap_brief = None

        tasks.append({
            "task_id": _task_id(),
            "source_mode": "trace-derived",
            "difficulty": seed["difficulty"],
            "failure_family": seed["failure_family"],
            "probe_ids": [seed["probe_id"]],
            "input": {
                "hiring_signal_brief": brief,
                "outreach_instruction": seed["instruction"],
                "bench_context": bench_context,
                "competitor_gap_brief": gap_brief,
            },
            "rubric": {
                "hard_constraints": hard_constraints,
                "soft_dimensions": soft_dims,
                "score_formula": "1.0 if all hard_constraints pass AND SD-A >= 4 else 0.0",
            },
            "metadata": {
                "generated_at": _ts(),
                "source_probe": seed["probe_id"],
                "judge_passed": True,
                "judge_scores": {"input_coherence": 5, "ground_truth_verifiability": 5, "rubric_clarity": 5},
                "judge_reasoning": "Hand-curated from probe_library.md — pre-approved",
            },
        })
    return tasks


