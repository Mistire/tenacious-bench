OPENROUTER_API_KEY = os.getenv("OPENROUTER_API_KEY", "")
OPENROUTER_URL = "https://openrouter.ai/api/v1/chat/completions"
GEN_MODEL = os.getenv("OPENROUTER_MODEL", "qwen/qwen3-235b-a22b")

_counter = 1000  # trace-derived start at TB-1000


def _task_id() -> str:
    global _counter
    tid = f"TB-{_counter:04d}"
    _counter += 1
    return tid


def _ts():
    return datetime.now(timezone.utc).isoformat()


def _call_llm(system: str, user: str, model: str = GEN_MODEL,
              max_tokens: int = 600, temperature: float = 0.7) -> str:
    if not OPENROUTER_API_KEY:
        raise RuntimeError("OPENROUTER_API_KEY not set")
    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
        "max_tokens": max_tokens,
        "temperature": temperature,
    }
    headers = {"Authorization": f"Bearer {OPENROUTER_API_KEY}", "Content-Type": "application/json"}
    resp = requests.post(OPENROUTER_URL, json=payload, headers=headers, timeout=45)
    resp.raise_for_status()
    return resp.json()["choices"][0]["message"]["content"].strip()


