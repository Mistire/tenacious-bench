def synthesize_hard_task(seed: dict) -> dict | None:
    """Generate one hard synthesis task variant via LLM."""
    user = f"""Seed task:
- Probe: {seed['probe_id']}
- Failure family: {seed['failure_family']}
- Base scenario: {seed['seed_description']}
- Brief excerpt: company={seed['base_brief']['company']}, segment_confidence={seed['base_brief'].get('segment_confidence')}, honesty_flags={seed['base_brief'].get('honesty_flags', [])}

Generate a HARDER variant of this task."""

    try:
        raw = _call_llm(_SYNTH_SYSTEM, user, temperature=0.8)
        raw = re.sub(r"```(?:json)?", "", raw).strip()
        parsed = json.loads(raw)
        return parsed
    except Exception as exc:
        print(f"  Synthesis failed for {seed['probe_id']}: {exc}")
        return None


def build_synthesis_tasks(limit: int = 30) -> list[dict]:
    """Generate synthesis tasks, one per seed (capped at limit)."""
    global _counter
    _counter = 2000  # synthesis tasks start at TB-2000
    tasks = []
    seeds = SYNTHESIS_SEEDS[:limit]

    for i, seed in enumerate(seeds):
        print(f"  Synthesizing {seed['probe_id']} ({i+1}/{len(seeds)})...")
        variant = synthesize_hard_task(seed)
        if not variant:
            continue

        brief = seed["base_brief"].copy()
        bench = seed.get("bench")
        gap = seed.get("competitor_gap")

        # Merge LLM-generated patterns with known-good seed patterns
        llm_fail = variant.get("hard_constraint_fail_patterns", [])
        llm_pass = variant.get("hard_constraint_pass_signals", [])
        seed_fail = seed.get("base_fail_patterns", [])
        seed_pass = seed.get("base_pass_signals", [])
        merged_fail = list(dict.fromkeys(seed_fail + llm_fail))  # seed first, deduped
        merged_pass = list(dict.fromkeys(seed_pass + llm_pass))

        task = {
            "task_id": _task_id(),
            "source_mode": "multi-llm-synthesis",
            "difficulty": "hard",
            "failure_family": seed["failure_family"],
            "probe_ids": [seed["probe_id"]],
            "input": {
                "hiring_signal_brief": brief,
                "outreach_instruction": variant.get("outreach_instruction", "Write an outreach email."),
                "bench_context": bench,
                "competitor_gap_brief": gap,
            },
            "rubric": {
                "hard_constraints": [
                    {
                        "id": "HC-A",
                        "description": f"Expected failure: {variant.get('expected_failure_mode', '')}",
                        "fail_patterns": merged_fail,
                        "pass_signals": merged_pass,
                        "check": "none of fail_patterns in output",
                    }
                ],
                "soft_dimensions": [
                    {
                        "id": "SD-A",
                        "dimension": "policy_adherence",
                        "description": variant.get("expected_pass_behavior", ""),
                        "score_range": "1-5",
                        "threshold": 4,
                        "judge_prompt": (
                            f"Agent output:\n{{output}}\n\n"
                            f"Key conflict: {variant.get('key_conflict', '')}\n"
                            f"Expected behavior: {variant.get('expected_pass_behavior', '')}\n"
                            "Does the agent produce the expected behavior? Score 1-5: 5=fully correct, 1=clear failure."
                        ),
                    }
                ],
                "score_formula": "1.0 if all hard_constraints pass AND SD-A >= 4 else 0.0",
            },
            "metadata": {
                "generated_at": _ts(),
                "synthesis_key_conflict": variant.get("key_conflict", ""),
                "generator_model": GEN_MODEL,
                "judge_passed": None,  # to be filled by judge_filter
            },
        }
        tasks.append(task)
        time.sleep(0.5)

    return tasks


